    public void mostrarDatos(){
        DefaultTableModel tjugador = new DefaultTableModel();
        tjugador.addColumn("NOMBRE");
        tjugador.addColumn("DNI");
        tjugador.addColumn("POSICION");
        tjugador.addColumn("DORSAL");
        this.jTableJugadores.setModel(tjugador);
        
        String []datos = new String[4];
        
        try{
            Statement leer = conect.createStatement();
            ResultSet resultado = leer.executeQuery("select * from jugadores");
            
            while(resultado.next()){
                
                datos[0] = resultado.getString(1);
                datos[1] = resultado.getString(2);
                datos[2] = resultado.getString(3);
                datos[3] = resultado.getString(4);
                tjugador.addRow(datos);
            }
            this.jTableJugadores.setModel(tjugador);
        }catch(Exception e){
            
        }  
            
        
    }

