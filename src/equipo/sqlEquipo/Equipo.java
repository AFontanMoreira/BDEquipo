    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       try{
           Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_equipo","root","");
            PreparedStatement pst = cn.prepareStatement("select * from jugadores where DNI = ?");
            
            pst.setString(1, this.jTextFieldSearch.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                this.jTextFieldNombre.setText(rs.getString("Nombre"));
                this.jTextFieldDNI.setText(rs.getString("DNI"));
                this.jTextFieldPosicion.setText(rs.getString("Posicion"));
                this.jTextFieldDorsal.setText(rs.getString("Dorsal"));
                this.jTextFieldSearch.setText("");
                
            }else{
                JOptionPane.showMessageDialog(null, "Jugador no registrado");
            }
       }catch(Exception e){
           
       }
    }//GEN-LAST:event_jButton4ActionPerformed

